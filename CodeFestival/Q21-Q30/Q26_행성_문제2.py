planet = input()

if planet == '수성':
    print("Mercury")
elif planet == '금성':
    print("Venus")    
elif planet == '지구':
    print("Earth")
elif planet == '화성':
    print("Mars")
elif planet == '목성':
    print("Jupiter")
elif planet == '토성':
    print("Saturn")
elif planet == '천왕성':
    print("Uranus")
elif planet == '해왕성':
    print("Neptune")
else:
    print("Not a planet")                          

